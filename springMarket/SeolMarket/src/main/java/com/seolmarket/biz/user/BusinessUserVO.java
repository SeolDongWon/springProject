package com.seolmarket.biz.user;

public class BusinessUserVO {
	private int businessMemberNo;
	private String businessMemberId;
	private String businessMemberPassword;
	private String businessMemberName;
	private String businessMemberTel;
	private String businessMemberAddress;
	private String businessMemberRegDate;

	public int getBusinessMemberNo() {
		return businessMemberNo;
	}

	public void setBusinessMemberNo(int businessMemberNo) {
		this.businessMemberNo = businessMemberNo;
	}

	public String getBusinessMemberId() {
		return businessMemberId;
	}

	public void setBusinessMemberId(String businessMemberId) {
		this.businessMemberId = businessMemberId;
	}

	public String getBusinessMemberPassword() {
		return businessMemberPassword;
	}

	public void setBusinessMemberPassword(String businessMemberPassword) {
		this.businessMemberPassword = businessMemberPassword;
	}

	public String getBusinessMemberName() {
		return businessMemberName;
	}

	public void setBusinessMemberName(String businessMemberName) {
		this.businessMemberName = businessMemberName;
	}

	public String getBusinessMemberTel() {
		return businessMemberTel;
	}

	public void setBusinessMemberTel(String businessMemberTel) {
		this.businessMemberTel = businessMemberTel;
	}

	public String getBusinessMemberAddress() {
		return businessMemberAddress;
	}

	public void setBusinessMemberAddress(String businessMemberAddress) {
		this.businessMemberAddress = businessMemberAddress;
	}

	public String getBusinessMemberRegDate() {
		return businessMemberRegDate;
	}

	public void setBusinessMemberRegDate(String businessMemberRegDate) {
		this.businessMemberRegDate = businessMemberRegDate;
	}

	@Override
	public String toString() {
		return "BusinessMemberVO [businessMemberNo=" + businessMemberNo + ", businessMemberId=" + businessMemberId
				+ ", businessMemberPassword=" + businessMemberPassword + ", businessMemberName=" + businessMemberName
				+ ", businessMemberTel=" + businessMemberTel + ", businessMemberAddress=" + businessMemberAddress
				+ ", businessMemberRegDate=" + businessMemberRegDate + "]";
	}

}
